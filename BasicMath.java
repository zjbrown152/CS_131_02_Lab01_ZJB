
public class BasicMath 
{
	private BasicMath()
	{
		
	}
	public static int add(int x, int y)
	{
		int sum=x+y;
		return sum;
	}
	public static int subtract(int x, int y)
	{
		int diff=x-y;
		return diff;
	}
	public static int multiply(int x, int y)
	{
		int product=x*y;
		return product;
	}
	public static double divide(double x, double y)
	{
		double quotient=x/y;
		return quotient;
	}

}
